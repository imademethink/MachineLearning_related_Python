#!/usr/bin/env python3
# -*- coding: utf-8 -*-

# number-integer
int_a = 10
int_b = -5
int_c = 0o6645			# 0o prefix for octal
int_d = -0x11			# 0x prefix for hex
print("int_a=" , int_a)
print("int_b=" , int_b)
print("int_c=" , int_c)
print("int_d=" , int_d)
print("\n")
# number-long
long_a = 10
long_b = -5
long_c = 0o6645			# 0o prefix for octal
long_d = -0x11			# 0x prefix for hex
print("long_a=" , long_a)
print("long_b=" , long_b)
print("long_c=" , long_c)
print("long_d=" , long_d)
print("\n")
# number-float
float_a = 10
float_b = -5.5
float_c = .9
float_d = 9.
float_e = 3.6e10
print("float_a=" , float_a)
print("float_b=" , float_b)
print("float_c=" , float_c)
print("float_d=" , float_d)
print("float_e=" , float_e)
print("\n")
# number-complex
complex_a = 10 + 5j
print("complex_a=" , complex_a)


